package day10;

public class Car {
	// Access modifiers
	// 1. public - accessible from everywhere
	// 2. protected - accessible within the same package and subclasses
	// 3. default(empty) - accessible within the same package only
	// 4. private - accessible within the same class only
	
	public String model;
	protected int year;
	String color;
	private double price;		
}
