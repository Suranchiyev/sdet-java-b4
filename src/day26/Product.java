package day26;

public class Product {
	public String name;
	public String category;
	public double price;
	public int count;
	
	public Product() {
		
	}
	
	public Product(String name, String category, double price, int count) {
		this.name = name;
		this.category = category;
		this.price = price;
		this.count = count;
	}
}
