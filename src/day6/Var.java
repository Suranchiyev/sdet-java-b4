package day6;

public class Var {
	
	public static void main(String[] args) {
		
		// container that can hold a data.
		
		// declare = create variable
		
		// DataType nameOfVariable = value;
		
		String str = "apple";
		
		System.out.println(str); // apple
		
		str = "orange";
		System.out.println(str); // orange
		// System.out.println(bek); // it will compile
		
		String bek = "banana";
		 
		System.out.println(str); // orange
		System.out.println(bek); // banana
		
		// DataType nameOfVariable = value;
		
		// we declared a variable of type int
		int i = 9;
		System.out.println(i); // 9
		
		i = 19;
		
		double i2;
		i2 = 19.9;
		System.out.println(i2);
		
		// String -> sequence of characters between ""
		// primitives:
		// byte, short, int, long -> whole numbers
		// float, double          -> floating numbers
		// char                   -> char
		// boolean                -> true, false
		
		byte num = 5;
		char ch = 'a';
		boolean isTrue = true;
	}
}
