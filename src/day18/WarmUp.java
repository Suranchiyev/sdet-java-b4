package day18;

public class WarmUp {
	public static void main(String[] args) {
		// 1. Print numbers from 0 to 100 by using for loop
		for (int i = 0; i <= 100; i++) {
			System.out.print(i + " ");
		}
		
		System.out.println();
				
		// 2. Print numbers from  100 to 0 by using for loop
		for (int i = 100; i >= 0; i--) {
			System.out.print(i + " ");
		}
		
		// When you will use while and when for loop?
		// - when we know exact number of iterations, we use for loop
		// - when we don't know exact number of iterations, we use
		//   while loops.
	}
}
