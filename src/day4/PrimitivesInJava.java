package day4;

public class PrimitivesInJava {
	public static void main(String[] args) {
		// primitives: 8
		// 1. Primitives for whole numbers: byte, short, int, long
		byte bNum = 10;
		short shNum = 101;
		int number = 999;
		long lNum = 7293482930000L;
		// we need L to tell java that it's long value, not int
		
		// 2. Primitives for floating numbers: float, double 
		float fNum = 99.9F;
		double dNum = 999.1;
		
		// 3. Primitive to store single character
		char ch = 'a';
		
		// 4. For logical value
		boolean isCorrect = false;
		boolean b1 = true;
	}
}
